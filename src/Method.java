public class Method {
    public static void main(String[] args) {
        // Diatas adalah contoh method dari main
        // contoh method yang lain adalah println()
        thisHelloWorld("Ahmad", "Dzulfikar", "Delf");
    }

    static void thisHelloWorld(String firstName, String lastName, String nickName) {
        System.out.println("Haloooooo nama saya " + firstName + " " + lastName + ", bisa dipanggil " + nickName);
    }
}
